using RentH2.Web.Models;
using RentH2.Web.Services.IServices;
using Newtonsoft.Json;
using System.Net;
using System.Text;
using static RentH2.Domain.Utility.SD;
using RentH2.Domain.Models;

namespace RentH2.Web.Service
